#include "3-calc.h"

/**
 * op_add - Function that returns the sum of a and b
 * @a: First integer operand
 * @b: Second integer operand
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that returns the difference between a and b
 * @a: First integer operand
 * @b: Second integer operand
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that returns the product of a and b
 * @a: First integer value
 * @b: Second integer value
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that returns the result of a divided by b
 * @a: First integer value
 * @b: Second integer value
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that returns the reamainder from the division
 * of a by b
 * @a: First integer operand
 * @b: Second integer operand
 *
 * Return: a % b
 */
int op_mod(int a,  int b)
{
	return (a % b);
}
